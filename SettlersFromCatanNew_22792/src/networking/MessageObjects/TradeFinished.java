package networking.MessageObjects;

import com.google.gson.annotations.SerializedName;

public class TradeFinished {
	
	@SerializedName("Spieler") private int player;
	@SerializedName("Mitspieler") private int otherPlayer;
	
	public TradeFinished(int player, int otherPlayer) {
		this.player = player;
		this.otherPlayer = otherPlayer;
	}

	public int getPlayer() {
		return player;
	}

	public void setPlayer(int player) {
		this.player = player;
	}

	public int getOtherPlayer() {
		return otherPlayer;
	}

	public void setOtherPlayer(int otherPlayer) {
		this.otherPlayer = otherPlayer;
	}
}
