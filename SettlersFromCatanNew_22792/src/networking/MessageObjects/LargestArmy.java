package networking.MessageObjects;

import com.google.gson.annotations.SerializedName;
/**
 * Class used to create the largest army messages (sent to the player with the most knight cards played)
 * @author Marcelina
 */
public class LargestArmy {
	@SerializedName("Spieler") Integer playerId;
	public LargestArmy(Integer playerId) {
		this.playerId = playerId;
	}
	public Integer getPlayerId() {
		return playerId;
	}

	public void setPlayerId(Integer playerId) {
		this.playerId = playerId;
	}
}
