package tools;
/**
 * Sets all the available resource types.
 */
public enum ResourceType {
    WOOD,WOOL,GRAIN,LOAM,STONE,HIDDEN;

	@Override
	public String toString() {
		switch (this) {
		case GRAIN:
			return "Grain";
		case LOAM:
			return "Loam";
		case STONE:
			return "Stone";
		case WOOD:
			return "Wood";
		case WOOL:
			return "Wool";
		case HIDDEN:
			return "Hidden";
		default:
			return "ResourceType{}";
		}
	}
}
